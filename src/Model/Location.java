package Model;

/**
 *
 * @author syamcode
 */
public class Location {
    private float lat;
    private float lng;

    /**
     * @return the lat
     */
    public float getLat() {
        return lat;
    }

    /**
     * @param lat the lat to set
     */
    public void setLat(float lat) {
        this.lat = lat;
    }

    /**
     * @return the lng
     */
    public float getLng() {
        return lng;
    }

    /**
     * @param lng the lng to set
     */
    public void setLng(float lng) {
        this.lng = lng;
    }
}
